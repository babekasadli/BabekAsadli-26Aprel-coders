package may11;

public class MathOperations {
    public static int add(int a,int b){
        return a+b;
    }
    public static int subtact(int a, int b){
        return a-b;
    }
    public static int multiply(int a,int b){
        return a*b;
    }
    public static double divide(double a,double b){
        return a /b;
    }

}
